package org.example;

public class Frame{
  private int fstPull;
  private int sndPull;
  private int bonus;

  public Frame(){ }

  public int getFstPull(){
    return fstPull;
  }

  public int getSndPull(){
    return fstPull;
  }

  public int getBonus(){
    return bonus;
  }

  public void setFstPull(int fstPull){
    this.fstPull = fstPull;
  }

  public void setSndPull(int sndPull){
    this.sndPull = sndPull;
  }

  public void setBonus(int bonus){
    this.bonus = bonus;
  }

}
